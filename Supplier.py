class Supplier:

    def __init__(self, id_supplier, name, phone_number, email, address, shipping_conditions):
        self.id_supplier = id_supplier  #Inicializa el ID del proveedor
        self.name = name  #Inicializa el nombre 
        self.phone_number = phone_number #Inicializa el numero de telefono del proveedor
        self.email = email #Inicializa el correo electronico del proveedor.
        self.address = address  #Inicializa la direccion del proveedor
        self.shipping_conditions = shipping_conditions  #Inicializa las condiciones de envio del proveedor.


